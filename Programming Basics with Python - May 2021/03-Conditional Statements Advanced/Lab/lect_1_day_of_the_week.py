weekday_number = int(input())

if weekday_number == 1:
    print('Monday')
elif weekday_number == 2:
    print('Tuesday')
elif weekday_number == 3:
    print('Wednesday')
elif weekday_number == 4:
    print('Thursday')
elif weekday_number == 5:
    print('Friday')
elif weekday_number == 6:
    print('Saturday')
elif weekday_number == 7:
    print('Sunday')
else:
    print('Error')
